package Module303_4;

public class IfStatementsExample {
    public static void main(String[] args) {
        double radius=25;
        if(radius>25){
            System.out.println("The radius is greater than 25");
        }else if(radius<25){
            System.out.println("The radius is less than 25");
        }else{
            System.out.println("The radius is equal to 25");
        }
        System.out.println("Done");
    }
}
