package Homework.PA303_2_1;

public class DivisionByDouble {
    public static void main(String[] args) {
        int num1=20;
        int num2=5;
        int division=num1/num2;
        System.out.println("Integer division result: " + division);

        double num1Decimal = 20.0;
        double divisionDecimal = num1Decimal / num2;
        System.out.println("Decimal division result: " + divisionDecimal);

    }
}
