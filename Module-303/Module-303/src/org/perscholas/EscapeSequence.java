package org.perscholas;

public class EscapeSequence {
    public static void main(String[] args) {
        System.out.println("This is line1 \nThis is line2 \nThis is line3");
        System.out.println("This is a tab 1\n");
        System.out.println("This is a tab 12 \t 123 \t 1234");
        System.out.println("this is using \" in the string");
        System.out.println("this is using \\ in the string");
        String s1="sushma";
        String s2=new String("sushma");
        System.out.println(s2.toUpperCase());
    }
}
